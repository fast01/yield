Object& HTTPRequestParser::parse() {
  if (p < eof) {
    ps = p;

    float http_version;
    HTTPRequest::Method method;
    iovec uri_fragment = { 0 };
    iovec uri_host = { 0 };
    iovec uri_path = { 0 };
    uint16_t uri_port = 0;
    iovec uri_query = { 0 };
    iovec uri_scheme = { 0 };
    iovec uri_userinfo = { 0 };

    if
    (
      parse_request_line(
        http_version,
        method,
        uri_fragment,
        uri_host,
        uri_path,
        uri_port,
        uri_query,
        uri_scheme,
        uri_userinfo
      )
    ) {
      URI uri(
        buffer,
        uri_fragment,
        uri_host,
        uri_path,
        uri_port,
        uri_query,
        uri_scheme,
        uri_userinfo
      );

      uint16_t fields_offset;
      size_t content_length;

      if (parse_fields(fields_offset, content_length)) {
        void* body;

        if (parse_body(content_length, body)) {
          return *new HTTPRequest(
                   body,
                   buffer,
                   connection_id,
                   content_length,
                   fields_offset,
                   http_version,
                   method,
                   uri
                 );
        } else {
          Buffer& next_buffer
          = Buffer::copy(
              Buffer::getpagesize(),
              p - ps + content_length,
              ps,
              eof - ps
            );
          ps = p;
          return next_buffer;
        }
      }
    } else { // cs == request_line_parser_error
      Object* object = parse_body_chunk();
      if (object != NULL)
        return *object;
    }

    if (p == eof) { // EOF parsing
      Buffer& next_buffer
      = Buffer::copy(
          Buffer::getpagesize(),
          eof - ps + Buffer::getpagesize(),
          ps,
          eof - ps
        );
      p = ps;
      return next_buffer;
    } else // Error parsing
      return *new HTTPResponse(400, NULL, http_version);
  } else // p == eof
    return *new Buffer(Buffer::getpagesize(), Buffer::getpagesize());
}

